nota = 87

print("Sistema de Calificaciones")
if nota >= 90:
    print("Calificacion: Sobresaliente")
elif nota >= 80:
    print("Calificacion: Notable")
elif nota >= 70:
    print("Calificacion: Aprobado")
else:
    print("Calificacion: Suspenso")